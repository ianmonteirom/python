j = input('Digite algo')
print('O que você digitou é uma letra do alfabeto?',j.isalpha())
print('O que você digitou é um número?',j.isnumeric())
print('O que você digitou é alfanumérico?',j.isalnum())
print('O que você digitou está em caps?',j.isupper())
print('O que você digitou está em minúsculo?',j.islower())
print('O que você digitou está em ascii?',j.isascii())
print('O que você digitou é um decimal?',j.isdecimal())
print('O que você digitou é um dígito?',j.isdigit())
print('O que você digitou é um espaço?',j.isspace())











